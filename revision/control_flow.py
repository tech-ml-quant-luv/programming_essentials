# Modulo operator
""" Module is a binary operator, and it gives the remainder. """

print(10/3)
print(10%2) #Module operator gives us the remainder
print(10//3)    #This operator gives us the quotient
print(10%2)

if (int(input("Enter your number: ")))%2==0:
    print("This is an even number")
else:
    print("This is an odd number")